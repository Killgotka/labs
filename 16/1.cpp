#include <iostream>

int main()
{
 int a[10];
    int n, i=1,i2=1;
    scanf_s("%d",&n);
    for (i=0; i<n; ++i, i2+=2) a[i]=i2;
    for (i=0; i<n; ++i) printf_s("%d : %d\n",i,a[i]);
}

