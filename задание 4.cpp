#include <iostream>
#include <math.h>
#define _CRT_SECURE_NO_WARMINGS

void main()
{
	int s, p, r, a, b;
	float d;
	a = 0;
	b = 0;
	printf_s("a=", a);
	scanf_s("%d", &a);
	printf_s("\nb=", b);
	scanf_s("%d", &b);
	s = pow(a, 2) + pow(b, 2);
	p = pow(a, 2) * pow(b, 2);
	r = pow(a, 2) - pow(b, 2);
	d = pow(a, 2) / pow(b, 2);
	printf_s("\ns=%d", s);
	printf_s("\np=%d", p);
	printf_s("\nr=%d", r);
	printf_s("\nd=%f", d);

}

